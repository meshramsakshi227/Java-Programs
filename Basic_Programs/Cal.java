public class Cal 
{
    public static void main(String[] args) 
    {
        //Integer Operation
        int a = 10;
        int b = 50;
        System.out.println("\n--- Integer Operators ---");
        System.out.println("Addition = " + (a + b));
        System.out.println("Subtraction = " + (a - b));
        System.out.println("Multiplication = " + (a * b));
        System.out.println("Division = " + (a / b));
        System.out.println("Modulus = " + (a % b));

        //floating point operation
        float x = 5.5f;
        float y = 2.3f;
        System.out.println("\n--- Floating point Operators ---");
        System.out.println("Addition = " + (x + y));
        System.out.println("Subtraction = " + (x - y));
        System.out.println("Multiplication = " + (x * y));
        System.out.println("Division = " + (x / y));
        System.out.println("Modulus = " + (x % y));

        //Double operation
        double p = 5.5;
        double q = 2.3;
        System.out.println("\n--- Floating point Operators ---");
        System.out.println("Addition = " + (p + q));
        System.out.println("Subtraction = " + (p - q));
        System.out.println("Multiplication = " + (p * q));
        System.out.println("Division = " + (p / q));
        System.out.println("Modulus = " + (p % q));

        //Character operation
        char c1 = 'A';
        char c2 = 'B';
        System.out.println("\n----Character Operation----");
        System.out.println("c1: " + c1);
        System.out.println("c2: " + c2);

        //Boolean
        System.out.println("Boolean flag: " + true);
        System.out.println("====Code Execution successful====");

    }
}
