public class Cond_ifelse_ifelse {

    public static void main(String[] args) {
        int number = -5;

        if (number > 0) {
            System.out.println("Positive Number");
        } else if (number == 0) {
            System.out.println("Zero");
        } else {
            System.out.println("Negative Number");
        }
    }
}

